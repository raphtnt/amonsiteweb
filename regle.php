  <h1>Règles policiers :</h1>

  -       Les forces de l’ordre ont une hiérarchie, elle doit être respectée quoi qu’il en soit.
<br>
  -       Les promotions sont accordées par le chef après concertation avec les officiers et le staff.
<br>
  -       Les forces de l’ordre opèrent sur tout le territoire d’Altis.
<br>
  -       Les forces de l’ordre doivent obligatoirement avoir la tenue réglementaire (uniforme, tazer, gilet pare-balles et arme létale).
<br>
  -       Aucune alliance ne peut être passée avec les rebelles. Il est possible d’avoir des indics’ aidant la police.
<br>
  -       Vous devez obligatoirement être dans le channel du Teamspeak dédié aux forces de l’ordre.
<br>
  -       Les forces de l’ordre ne connaissent pas l’emplacement des ressources illégales ainsi que leur traitement et le revendeur. Il est cependant possible d’effectuer des raids sur ces zones si une filature ou un tuyau ont permis de les repérer. Le camp rebelle est compris dans ces zones.
<br>
  -       Lors de raids, les policiers doivent obligatoirement repartir au bout de 20 minutes d’occupation de la zone.
<br>
  -       Les forces de l’ordre ne peuvent connaître la position des maisons d’une personne uniquement s’ils ont vu cette personne y rentrer.
<br>
  -       Il est interdit d’arrêter ou de fouiller une personne sans aucune raison valable.
<br>
  -       Il est obligatoire de préciser les faits reprochés aux personnes avant de les mettre en prison.
<br>
  -       Il est interdit de refuser un avocat à une personne en état d’arrestation.
<br>
  -       Lors de son arrivée, il est obligatoire de demander à son supérieur, sur le Teamspeak, son affectation.
<br>
  -       Le drone est réservé au corps d’élite de la police. Il ne peut être utilisé que par les membres de celui-ci lors, d’une action RP uniquement, dans un but de reconnaissance des lieux (lors de raids, de prise d’otages, d’attaque de banque).
<br><br><br>


  <h1>Règles rebelles :</h1>

  -       Être rebelle n’est pas un motif pour tirer sur tout ce qui bouge ! Il est obligatoire de procéder à une sommation avant d’ouvrir le feu dans le but de tuer.
<br>
  -       Un même gang ne doit pas braquer plusieurs fois les mêmes personnes. Pour re-braquer quelqu’un, 2 heures minimum doivent s’être écoulées.
<br>
  -       Les bases rebelles ne sont pas des Safe Zone.
<br>
  -       Les rebelles peuvent défendre leur base avec tous les moyens en leur possession.
<br>
  -       Les rebelles peuvent attaquer les bases des familles adverses.
<br>
  -       Seules les familles connaissent la position de leur propre base.
<br>
  -       Toute personne se trouvant à proximité d’une base rebelle pourra être exécuté par le gang détenant la base.
<br>
  -       Lors d’une attaque de douane, il est obligatoire d’envoyer un négociateur prendre contact avec la police afin de négocier la capture de cette dernière. Tant que les négociations ne sont pas finies, aucun meurtre ne doit avoir lieu !
<br>
  -       Les retours sur zone sont interdits, même après 5/10/15/30minutes ! D'un point de vue RP, on ne voit jamais 2 fois la même personne sur une action, surtout si elle a été tuée !
<br>
  -       La douane ne peut pas être tenue plus de 10 minutes. Au bout de ce délai, les rebelles ont obligation de quitter les lieux.
<br>
  -       Il est interdit d’obliger un civil à aller retirer de l’argent pour le lui prendre. De même, il est interdit de rester devant un ATM en attendant que quelqu’un retire de l’argent.
<br>
  -       Lors du braquage d’un civil, vous ne pouvez pas prendre plus de la moitié de l’argent liquide qu’il a sur lui ainsi que plus de la moitié de la marchandise présente dans son moyen de transport (camion, voiture, hélicoptère, etc…).
<br>
  -       Il doit y avoir au minimum 5 policiers de connectés pour braquer la banque.
<br>
  -    Il doit y avoir au minimum 3 policiers de connectés pour une prise d'otages.
<br>
  -       Il est obligatoire d’attendre le prochain reboot pour pouvoir de nouveau braquer la banque.
<br>
  -       Il ne peut pas y avoir plusieurs grosses actions RP en même temps. Si une prise d’otage a lieu, il faut attendre 20 minutes après la fin de celle-ci pour pouvoir refaire une grosse action RP (braquage de banque, prise d’otage, attaque de douane, etc…).
<br>
  -       Il faut attendre 30 minutes après le reboot pour pouvoir commencer une grosse action afin de laisser le temps aux forces de l’ordre de se reconnecter !
<br>
  -   Les grosses actions RP sont: Attaque de douane, prises d'otages, braquage de banque, toutes opérations nécessitant d'importants moyens policiers/GIGA.
<br>
  -       Lorsque vous donnez un ordre à un civil ou un otage, vous devez le répéter 3 fois avant de considérer que celui-ci refuse de vous écouter.
<br>
  -     Lorsqu'un civil coopère, vous n'avez pas le droit de voler son véhicule et de prendre plus de la moitié de son argent/sa marchandise.
<br>
  -       En cas de refus d’obtempérer, vous avez le droit de lui prendre tout ce qu’il a (argent, vêtement, armes, marchandise), que ce soit sur lui ou dans son véhicule.
<br>
  -      Si le civil tente de se défendre, vous pouvez l’abattre.
<br>
  -     Lors d'une attaque de douane, un message doit être envoyer avant l'attaque, en guise de sommation.
